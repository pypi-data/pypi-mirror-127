def printn(str_=""):
    """Prints a string without a trailing newline"""
    if objtype(str_) == "bytes":
        print(str_.decode("iso-8859-1"), end="", flush=True)
        
    else:
        print(str(str_), end="", flush=True)
        
    return 0

